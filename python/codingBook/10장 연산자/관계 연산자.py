# 대소관계 연산자는 > < >= <= == != 사용가능
# <     a < b   a가 b보다 작은지
# >     a > b   a가 b보다 큰지
# <=    a <= b  a가 b보다 같거나 작은지
# >=    a >= b  a가 b보다 같거나 큰지
# ==    a == b  a와 b의 값이 같은 값인지
# !=    a != b  a와 b의 값이 다른지
# 결과는 bool 값으로 반환
a = 15
b = 9
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)
print(a==b)
print(a!=b)